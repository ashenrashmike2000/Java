import java.util.*;
class comparison_operators{
	
	public static void main(String args[]){
		int x = 10;
		int y = 12;

		System.out.println((x==y));//Equal--->false
		System.out.println((x!=y));//not Equal--->true
		System.out.println((x>y));//Greaterthan--->false
		System.out.println((x<y));//Lessthan--->true
		System.out.println((x>=y));//Greater than or equal--->false
		System.out.println((x<=y));//Less than or Equal--->true
		
	}
}