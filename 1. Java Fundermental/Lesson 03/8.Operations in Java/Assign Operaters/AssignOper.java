import java.util.*;
class AssignOper{
	
	public static void main(String args[]){
		int x = 10;
		
		x+=10;
		System.out.println(" += " + x);//20
		x-=10;
		System.out.println(" += " + x);//10
		x*=10;
		System.out.println(" += " + x);//100
		x/=10;
		System.out.println(" += " + x);//10
		x%=10;
		System.out.println(" += " + x);//0
	
	}

}