import java.util.*;
class Logical_Operators{
	
	public static void main(String args[]){
			int x = 10;
			int y = 12;
		
		System.out.println((x<=10)&&(y<10));//Logical and ---> false
		System.out.println((x <= 10) || (y < 10)); //Logical or ---> true
		System.out.println(!((x <= 10) && (y < 10))); //Logical or --->

	}
}